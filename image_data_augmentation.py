def preprocess_image(dataset_path, image_size=150):
  images = []
  labels = []
  flowers = []
  image_info = []

  for flower_id, flower_name in enumerate(flower_names):
    flower_class_path = os.path.join(dataset_path, flower_name)


    image_files = []
    for file in os.listdir(flower_class_path):
      if file.lower().endswith(('.png', '.jpg', '.jpeg')):
        image_files.append(file)


    print(f"Total Images Found in {flower_name} : {len(image_files)}")

    for image_name in image_files:
      image_path = os.path.join(flower_class_path, image_name)
      image = Image.open(image_path)
      image = image.resize((image_size, image_size))
      image = image.convert('RGB')
      image = np.array(image) / 255
      images.append(image)
      labels.append(flower_id)

      info = {
          'filename' : image_name,
          'flower_name' : flower_name,
          'flower_id' : flower_id,
          'image_size ' : f"({image.shape[0]} * {image.shape[1]})",


      }
      image_info.append(info)

  images = np.array(images)


  labels = np.array(labels)

  data = pd.DataFrame(image_info)
  data.to_csv('image_info.csv', index=False)


  return images, labels, flower_names



