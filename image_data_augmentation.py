total_augmented = 0

def apply_augmentations(img_tensor):
    augmented_versions = []


    augmented_versions.append(img_tensor)

    augmented_versions.append(tf.image.flip_left_right(img_tensor))


    augmented_versions.append(tf.image.flip_up_down(img_tensor))

    if img_tensor.shape[-1] == 3:
        gray = tf.image.rgb_to_grayscale(img_tensor)
        gray = tf.image.grayscale_to_rgb(gray)  # convert back to 3 channels
        augmented_versions.append(gray)

    augmented_versions.append(tf.image.rot90(img_tensor))

    augmented_versions.append(tf.image.adjust_brightness(img_tensor, 0.3))

    return augmented_versions


for i in range(len(X)):
    try:

        img = X[i]


        if img.max() > 1.0:
            img = img / 255.0

        label = y[i]

        img_tensor = tf.convert_to_tensor(img, dtype=tf.float32)


        img_tensor = tf.clip_by_value(img_tensor, 0, 1)


        aug_versions = apply_augmentations(img_tensor)

        for j, aug_img in enumerate(aug_versions):

            aug_img = tf.clip_by_value(aug_img, 0, 1)
            np.save(f'augmented_dataset/img_{i}_{j}.npy', aug_img.numpy())
            np.save(f'augmented_labels/label_{i}_{j}.npy', label)
            total_augmented += 1

        if (i+1) % 10 == 0:
            print(f"Processed {i+1}/{len(X)} original images, Created {total_augmented} augmented images")

    except Exception as e:
        print(f"Error on image {i}: {e}")
        continue

print(f"Done! Total augmented images saved: {total_augmented}")